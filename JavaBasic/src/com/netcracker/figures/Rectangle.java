package com.netcracker.figures;

public class Rectangle {

    public static void main(String[] args) {
        int size = 5;

        for (int j = 0; j < size; j++) {
            for (int i = 0; i < size; i++) {
                System.out.print(" # ");
            }
            System.out.println("\n");
        }
    }
}
